    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'project_name' => 'required|string|max:255',
            'description' => 'required|string',
            'project_image' => 'required|image|mimes:jpeg,png,jpg,gif|max:2048', 
        ]);

        if ($request->hasFile('project_image')) {
            $imagePath = $request->file('project_image')->store('uploads','public');
        }

        project::create([
            'project_name' => $request->input('project_name'),
            'description' => $request->input('description'),
            'image' => isset($imagePath) ? $imagePath : null,
        ]);

        return redirect()->route('home')->with('success', 'Form data submitted successfully');


    }

